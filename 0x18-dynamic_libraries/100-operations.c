#include <stdio.h>
#include "main.h"

/**
* add - Add two integers.
* @a: The first integer.
* @b: The second integer.
*
* Return: The sum of a and b.
*/
int add(int a, int b)
{
return (a + b);
}

/**
* sub - Subtract two integers.
* @a: The first integer.
* @b: The second integer.
*
* Return: The difference between a and b.
*/
int sub(int a, int b)
{
return (a - b);
}

/**
* mul - Multiply two integers.
* @a: The first integer.
* @b: The second integer.
*
* Return: The product of a and b.
*/
int mul(int a, int b)
{
return (a * b);
}

/**
* div - Divide two integers.
* @a: The dividend.
* @b: The divisor.
*
* Return: The result of dividing a by b.
*/
int div(int a, int b)
{
return (a / b);
}

/**
* mod - Compute the remainder of division of two integers.
* @a: The dividend.
* @b: The divisor.
*
* Return: The remainder of dividing a by b.
*/
int mod(int a, int b)
{
return (a % b);
}
